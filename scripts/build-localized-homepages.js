function replaceAllLiteral(source, from, to) {
  return source.split(from).join(to);
}

function protectBlocks(html, pattern, prefix, store) {
  return html.replace(pattern, (match) => {
    const key = `___${prefix}_${store.length}___`;
    store.push(match);
    return key;
  });
}

function restoreBlocks(html, prefix, store) {
  store.forEach((value, index) => {
    html = html.replace(`___${prefix}_${index}___`, value);
  });
  return html;
}

function replaceVisibleText(html, translations) {
  const protectedScripts = [];
  const protectedSvg = [];
  html = protectBlocks(html, /<script[\s\S]*?<\/script>/gi, 'SCRIPT', protectedScripts);
  html = protectBlocks(html, /<svg[\s\S]*?<\/svg>/gi, 'SVG', protectedSvg);

  const entries = Object.entries(translations).sort((a, b) => b[0].length - a[0].length);
  html = html.replace(/>([^<>]+)</g, (match, text) => {
    let next = text;
    for (const [from, to] of entries) {
      next = replaceAllLiteral(next, from, to);
    }
    return `>${next}<`;
  });

  html = restoreBlocks(html, 'SVG', protectedSvg);
  html = restoreBlocks(html, 'SCRIPT', protectedScripts);
  return html;
}

function localizeLinks(html, code) {
  const servicePaths = [
    'online-intensive-programmes',
    'online-courses-russian-law',
    'language-lessons',
    'academic-coaching',
    'lessons'
  ];
  for (const slug of servicePaths) {
    html = html.replace(new RegExp(`href="/${slug}/"`, 'g'), `href="/${code}/${slug}/"`);
  }
  html = html.replace(/href="#(about|research|teaching|services|publications|contact)"/g, `href="/${code}/#$1"`);
  return html;
}

function updateLanguageSwitcher(html, current) {
  const labels = {
    en: 'EN · English',
    ar: 'AR · العربية',
    zh: 'ZH · 中文',
    fr: 'FR · Français',
    ru: 'RU · Русский',
    es: 'ES · Español'
  };
  const links = {
    en: '/',
    ar: '/ar/',
    zh: '/zh/',
    fr: '/fr/',
    ru: '/ru/',
    es: '/es/'
  };
  for (const [code, label] of Object.entries(labels)) {
    const href = links[code];
    html = html.replace(new RegExp(`<a href="[^"]*" class="block rounded-2xl px-4 py-2\\.5 hover:bg\\[#FAF9F6\\] text-\\[#0D3B66\\] font-semibold">${code.toUpperCase()}[^<]*</a>`, 'g'), `<a href="${href}" class="block rounded-2xl px-4 py-2.5 hover:bg-[#FAF9F6] text-[#0D3B66] font-semibold">${label}</a>`);
  }
  html = html.replace(/aria-label="Choose website language">EN/g, `aria-label="Choose website language">${current}`);
  return html;
}

